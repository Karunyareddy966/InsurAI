// OperatorPrograms/ArithmeticExample.java
public class ArithmeticExample {
    public static void main(String[] args) {
        int x = 7;
        int y = 4;
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y)); // integer division
        System.out.println("x % y = " + (x % y));
    }
}
